# Converter temperatura de Celsius para Fahrenheit
# - Fórmula: F = C * 9/5 + 32
# - Dica: peça entrada do usuário.
celsius_temperature = int(input('Informe a temperatura em Celsius: '))
fahrenheit = int(celsius_temperature * 9 / 5) + 32
print(f'Resultado em Fahrenheit: {fahrenheit}ºF')